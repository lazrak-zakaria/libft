#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	size_t	i;

	i = ft_strlen(s1);
	a = (char *)malloc(sizeof(char) * (i + 1));
	if (!a)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
